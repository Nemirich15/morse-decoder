const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let morseStr = expr.split(" ")

    for( let i = 0; i < morseStr.length; i++){
        if(morseStr[i] === "" &&  morseStr[i - 1] === ""){
            morseStr.splice(i, 2, " ")
        }
        if(morseStr[i] !== ""){
            morseStr[i] = MORSE_TABLE[morseStr[i]]
        } else {
            morseStr[i] = " "
        }
    }
    return morseStr.join("")
}

module.exports = {
    decode
}